    private void btnMultiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMultiActionPerformed
        estado = "*";
        if (numero.isEmpty()&&!concatenar.isEmpty()){
            asignacionInicial();     
        }else if(numero.size()==1&&concatenar.isEmpty()&&operacion.equals("resultado")){
            asignacionResultado();
        }else if(!numero.isEmpty()&&!concatenar.isEmpty()&&operacion.equals("*")){
            asignacionNumero();
            valor = String.valueOf(numero.get(0))+"*"+ concatenar + "=" ;
            resultado = numero.get(0)*numero.get(1);
            outResultado();
        }else if(numero.size()==1 && concatenar.isEmpty()){
            asignacionResultado();
        }else if(!numero.isEmpty()&&!concatenar.isEmpty()&&operacion.equals("resultado")){
            asignacionNumero();
            valor = String.valueOf(numero.get(0))+"*"+ concatenar + "=" ;
            resultado = numero.get(0)*numero.get(1);
            estado="";
            outResultado();
            
        }
        if(numero.size()==1 &&!concatenar.isEmpty()&&operacion.equals("+")){
            
            btnSuma.doClick();
            operacion="*";
            goToNewOperation();
            
        }else if(numero.size()==1 &&!concatenar.isEmpty()&&operacion.equals("-")){
          
            btnResta.doClick();
            operacion="*";
            goToNewOperation();
            
        }else if(numero.size()==1 &&!concatenar.isEmpty()&&operacion.equals("/")){
            
            btnDividir.doClick();
            operacion="*";
            goToNewOperation();
            
        }
    }//GEN-LAST:event_btnMultiActionPerformed

