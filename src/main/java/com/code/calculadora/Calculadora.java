    private void btnIgualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIgualActionPerformed
        switch (operacion) {
            case "+" -> {
                valorNumerico = Double.parseDouble(concatenar);
                total = numero.get(0) + valorNumerico;
                txtContenido.setText(String.valueOf(total));
                txtMostrar.setText(String.valueOf(numero.get(0))+"+" + String.valueOf(valorNumerico) + "=" );
                numero.clear();
                concatenar = "";
                numero.add(total);
                operacion = "resultado";
            }
            case "-" -> {
                valorNumerico = Double.parseDouble(concatenar);
                total = numero.get(0) - valorNumerico;
                txtContenido.setText(String.valueOf(total));
                txtMostrar.setText(String.valueOf(numero.get(0))+"-" + String.valueOf(valorNumerico) + "=");
                numero.clear();
                concatenar = "";
                numero.add(total);
                operacion="resultado";
            }
            case "*" -> {
                valorNumerico = Double.parseDouble(concatenar);
                total = numero.get(0) * valorNumerico;
                txtContenido.setText(String.valueOf(total));
                txtMostrar.setText(String.valueOf(numero.get(0))+"*" + String.valueOf(valorNumerico) + "=");
                numero.clear();
                concatenar = String.valueOf(total);
                numero.add(total);
                operacion="resultado";
            }
                
            case "/" -> {
                valorNumerico = Double.parseDouble(concatenar);
                total = numero.get(0) / valorNumerico;
                txtContenido.setText(String.valueOf(total));
                txtMostrar.setText(String.valueOf(numero.get(0))+"/" + String.valueOf(valorNumerico) + "=");
                numero.clear();
                concatenar = String.valueOf(total);
                numero.add(total);
                operacion="resultado";
            }
            
            case "resultado" -> {
                txtContenido.setText(String.valueOf(total));
                txtMostrar.setText(String.valueOf(total));
            }
            default ->{
                
            }
        }
    }//GEN-LAST:event_btnIgualActionPerformed

    private void btnRestaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRestaActionPerformed
        estado = "-";
        if (numero.isEmpty()&&!concatenar.isEmpty()){
            asignacionInicial();            
        }else if(numero.size()==1&&concatenar.isEmpty()&&operacion.equals("resultado")){
            asignacionResultado();
        }else if(!numero.isEmpty()&&!concatenar.isEmpty()&&operacion.equals("-")){
            asignacionNumero();
            valor = String.valueOf(numero.get(0))+"-"+ concatenar + "=" ;
            resultado = numero.get(0)- numero.get(1);
            outResultado();
        }else if(numero.size()==1 && concatenar.isEmpty()){
            asignacionResultado();
        }
        if(!numero.isEmpty()&&!concatenar.isEmpty()&&operacion.equals("resultado")){
            
            asignacionNumero();
            valor = String.valueOf(numero.get(0))+"-"+ concatenar + "=" ;
            resultado = numero.get(0)-numero.get(1);
            estado="";
            outResultado();      
        }
        if(numero.size()==1 &&!concatenar.isEmpty()&&operacion.equals("+")){
            
            btnSuma.doClick();
            operacion="-";
            goToNewOperation();
           
        }else if(numero.size()==1 &&!concatenar.isEmpty()&&operacion.equals("*")){
            
            btnMulti.doClick();
            operacion="-";
            goToNewOperation();
            
        }else if(numero.size()==1 &&!concatenar.isEmpty()&&operacion.equals("/")){
            
            btnDividir.doClick();
            operacion="-";
            goToNewOperation();
            
        }
    }//GEN-LAST:event_btnRestaActionPerformed

