    private void btnDelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDelActionPerformed
        String texto = txtContenido.getText();
        if(!texto.equals(String.valueOf(total))&&!operacion.equals("")&&!concatenar.isEmpty()&&!numero.isEmpty()){
            if(texto.isEmpty()){
                txtContenido.setText("");

            }else{
                txtContenido.setText(texto.substring(0, texto.length()-1));
            }
            if(!concatenar.isEmpty()){
                concatenar = concatenar.substring(0, concatenar.length()-1); 
            }else{
                concatenar = "";
            }
            if (operacion.equals("resultado")){
                txtMostrar.setText(String.valueOf(numero.get(0)));
            }else{
                txtMostrar.setText(String.valueOf(numero.get(0)+ operacion));
            }
            
        }else{
            txtContenido.setText("");
            concatenar = "";
            txtMostrar.setText(String.valueOf(total));
        }
        
    }//GEN-LAST:event_btnDelActionPerformed

