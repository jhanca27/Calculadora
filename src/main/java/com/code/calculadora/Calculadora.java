    public void asignacionInicial(){
        asignacionNumero();
        valor = concatenar + estado;
        txtMostrar.setText(valor);
        operacion = estado;
        concatenar = "";     
    }
    
    public void asignacionNumero(){
        valorNumerico = Double.parseDouble(concatenar);
        numero.add(valorNumerico);
    }
    
    public void asignacionResultado(){
        valor = String.valueOf(numero.get(0))+ estado;
        txtMostrar.setText(valor);
        operacion = estado;
        concatenar = "";
        System.out.println(operacion);
    }
    
    public void outResultado(){
        txtContenido.setText(String.valueOf(resultado));
        txtMostrar.setText(String.valueOf(resultado)+estado);
        numero.clear();
        numero.add(resultado);
        operacion = estado;
        concatenar="";
        
    }
    
    public void goToNewOperation(){
        txtContenido.setText(String.valueOf(resultado));
        txtMostrar.setText(String.valueOf(resultado)+operacion);
       
    }
